package com.example.peripheralapp.presentation.viewModel

/**
 * События пользовательского интерфейса Peripheral-приложения.
 *
 * Назначение:
 * - описывает действия пользователя, которые запускаются из интерфейса (кнопки);
 * - передаётся в модель представления, чтобы выполнить соответствующую команду доменного слоя.
 */
sealed interface UiEvent {
    /**
     * Запрос на запуск BLE-сервера периферийного устройства.
     *
     * Ожидаемое поведение обработчика:
     * - поднять GATT-сервер;
     * - включить advertising;
     * - обновить состояние `isRunning`, `lastError` и связанные признаки.
     */
    data object StartServer: UiEvent
    /**
     * Запрос на остановку BLE-сервера периферийного устройства.
     *
     * Ожидаемое поведение обработчика:
     * - остановить advertising;
     * - закрыть GATT-сервер и освободить ресурсы Bluetooth;
     * - обновить состояние `isRunning` и связанные признаки.
     */
    data object StopServer: UiEvent
    /**
     * Запрос на старт передачи данных (трансфер уведомлений Peripheral -> Central).
     *
     * Ожидаемое поведение обработчика:
     * - запустить периодическую отправку блоков данных по каналу DATA (уведомления);
     * - учитывать наличие подписки со стороны Central.
     */
    data object StartTransfer: UiEvent
    /**
     * Запрос на остановку передачи данных (трансфер уведомлений Peripheral -> Central).
     *
     * Ожидаемое поведение обработчика:
     * - остановить периодическую отправку данных;
     * - корректно завершить.
     */
    data object StopTransfer: UiEvent

}