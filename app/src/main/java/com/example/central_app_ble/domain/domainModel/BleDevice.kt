package com.example.central_app_ble.domain.domainModel

/**
 * Представление BLE-устройства в доменном слое.
 *
 * Назначение:
 * - хранит минимально необходимую информацию об устройстве, выбранном при сканировании;
 * - используется UI-слоем и доменными сценариями, не завися от Android-классов Bluetooth.
 *
 * Поля:
 * - [name] — имя устройства (может отсутствовать, тогда `null`);
 * - [address] — адрес устройства (уникальный идентификатор на уровне Bluetooth);
 * - [rssi] — уровень сигнала (условная «сила» приёма), по нему можно оценивать расстояние
 *   и сортировать устройства при выборе.
 */
data class BleDevice(
    val name: String?,
    val address: String,
    val rssi: Int // потом можно добавить сортировку устройств по дальности устройства от Central
)
